package HomeWork3;

public class Fruit {
    protected float weight;
    protected int number;

    public Fruit(float weight){
        this.weight = weight;
    }

    public int getNumber() {
        return number;
    }
    public float getWeight(){
        return weight;
    }

    public void setNumber(int number) {
        this.number = number;
    }
    public void setWeight(float weight) {
        this.weight = weight;
    }


}
